<template>
    <ul class="list-unstyled">
        <TodoListItem
            v-for="todoitem in todolist"
            :key="todoitem.id"
            :todoitem="todoitem"
            @toggle="toggleComplted"
            @delete="deleteTodo"
        >
        </TodoListItem>
    </ul>
</template>

<script setup>
import TodoListItem from "./TodoListItem.vue";

const props = defineProps({
    todolist: Array,
});

// App 컴포넌트로 보내는 emit
const emit = defineEmits(["toggle", "delete"]);

const toggleComplted = (id) => {
    emit("toggle", id);
};

const deleteTodo = (id) => {
    emit("delete", id);
};
</script>

